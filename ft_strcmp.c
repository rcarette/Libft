#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		size_s1_s2;

	size_s1_s2 = 0;
	while ((*s1 || *s2) && size_s1_s2 == 0)
		size_s1_s2 = *s1++ - *s2++;
	return (size_s1_s2);
}
